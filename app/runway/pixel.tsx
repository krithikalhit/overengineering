export function PixelText({
  text,
  scale = 3,
  color = "#000000",
  label,
}: {
  text: string;
  scale?: number;
  color?: string;
  label?: string;
}) {
  const ref = React.useRef<HTMLCanvasElement>(null);
  React.useEffect(() => {
    const canvas = ref.current;
    if (!canvas) return;
    const chars = text.toUpperCase().split("");
    const w = Math.max(1, chars.length * 6 * scale - scale);
    const h = 7 * scale;
    canvas.width = w;
    canvas.height = h;
    canvas.style.width = `${w}px`;
    canvas.style.height = `${h}px`;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    ctx.clearRect(0, 0, w, h);
    ctx.fillStyle = color;
    chars.forEach((ch, ci) => {
      const glyph = FONT[ch] ?? FONT[" "];
      for (let r = 0; r < 7; r++) {
        for (let b = 0; b < 5; b++) {
          if (glyph[r] & (1 << (4 - b))) {
            ctx.fillRect((ci * 6 + b) * scale, r * scale, scale, scale);
          }
        }
      }
    });
  }, [text, scale, color]);
  return <canvas ref={ref} className="rw-px" role="img" aria-label={label ?? text} />;
}

export function PixelHeart({ scale = 3, color = "#1414f0" }: { scale?: number; color?: string }) {
  const ref = React.useRef<HTMLCanvasElement>(null);
  React.useEffect(() => {
    const canvas = ref.current;
    if (!canvas) return;
    const rows = ["011101110", "111111111", "111111111", "111111111", "011111110", "001111100", "000111000", "000010000"];
    canvas.width = 9 * scale;
    canvas.height = 8 * scale;
    canvas.style.width = `${9 * scale}px`;
    canvas.style.height = `${8 * scale}px`;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    ctx.fillStyle = color;
    rows.forEach((row, r) =>
      row.split("").forEach((c, x) => {
        if (c === "1") ctx.fillRect(x * scale, r * scale, scale, scale);
      }),
    );
  }, [scale, color]);
  return <canvas ref={ref} className="rw-px" aria-hidden="true" />;
}
